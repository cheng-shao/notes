// optional chaining（可选链） ?.
// 通过连接的对象的引用或函数可能是 undefined 或 null 时，可选链操作符提供了一种方法来简化被连接对象的值访问。

// nullish coalescing（空值合并操作符） ??
// 是一个逻辑操作符，当左侧的操作数为 null 或者 undefined 时（不含空字符串和0等假值），返回其右侧操作数，否则返回左侧操作数。


